    private void txt_loanKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_loanKeyTyped
        char iNumber=evt.getKeyChar();
        if(!(Character.isDigit(iNumber))
                ||(iNumber==KeyEvent.VK_BACK_SPACE)
                ||(iNumber==KeyEvent.VK_DELETE))
        {
            evt.consume();
        }
            
    }//GEN-LAST:event_txt_loanKeyTyped

    private void btn_loanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_loanActionPerformed
       double annualInterestRate=Double.parseDouble(txt_interestRate.getText());
       double monthlyInterestRate=annualInterestRate/1200;
       int numberOfYear=Integer.parseInt(txt_year.getText());
       double loanAmount=Double.parseDouble(txt_loan.getText());
       
       //---------------------------------------------------------------
       double monthlyPayment=loanAmount*monthlyInterestRate/(1-1/Math.pow(1+monthlyInterestRate,numberOfYear*12));
       String imonthlyPayment;//,impnthpayment_format;
       imonthlyPayment=Double.toString(monthlyPayment).format("%.2f",monthlyPayment );
       
       
   
//       imonthlyPayment=String.format("%.2f",monthlyPayment);
       lbl_monthlyPayment.setText(imonthlyPayment);
       
      //--------------------------------------------------------------
      double totalPayment=monthlyPayment*numberOfYear*12;
      String itotalPayment;
      itotalPayment=String.format("%.2f",totalPayment);
      lbl_totalPayment.setText(itotalPayment);
      
       
       
       
    }//GEN-LAST:event_btn_loanActionPerformed


   private void inputtext(String text){
       
       if(selected_field == 1)
       {
            String enternumber=txt_interestRate.getText()+text;
            txt_interestRate.setText(enternumber);
       }
       else if(selected_field == 2){
           String enternumber=txt_year.getText()+text;
            txt_year.setText(enternumber);
       }
       else if(selected_field == 3)
       {
           String enternumber=txt_loan.getText()+text;
            txt_loan.setText(enternumber);
       
       }
   
   }
    
    
    
